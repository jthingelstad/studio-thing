"""Unified content pipeline — workshop-as-source.

Two responsibilities:

1. **Build** the static site's archive files (``apps/site/archive/{N}.md`` +
   ``apps/site/_data/emails.json``) from ``data/issues/{N}/`` — the canonical
   per-issue source that workshop_bot commits via its ship sequence. The
   ``build`` subcommand is what ``make build`` and the CI workflow invoke.

2. **Publish** the in-flight workshop issue to Buttondown as a draft.
   ``buttondown_publish_idempotent`` is wrapped by workshop_bot's
   ``send-to-buttondown`` job (and exposed via the ``publish`` CLI for
   manual use). It POSTs on first run, PATCHes on every subsequent run,
   and never sends ``publish_date`` — Jamie schedules and ships from the
   Buttondown UI.

The legacy ``data/buttondown/`` snapshot tree, its three-way push/pull/diff
machinery, and the Liquid-strip transform that turned raw Buttondown bodies
into website-shaped markdown were retired alongside the inversion. The
website's canonical source is now ``data/issues/{N}/archive.md``; the workshop
produces it directly, and the build path here just reads and re-emits with
the 11ty front-matter contract (layout / permalink / tags / audio_* injected
late).

A ``stats`` subcommand refreshes ``apps/site/_data/stats.json`` (subscriber
counts + Stripe balance) — used to be wedged into the pull path, now stands
alone so CI can call it explicitly without entangling with content reads.
"""

