"""Unified Buttondown content pipeline.

This script keeps a durable, tracked Buttondown baseline in data/buttondown,
builds the Eleventy archive from that baseline, and safely syncs local raw
body/metadata edits back to Buttondown with three-way conflict detection.
"""

